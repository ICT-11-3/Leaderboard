function displayStudents(students, selectedMonth) {
  const studentListDiv = document.getElementById("studentList");
  studentListDiv.innerHTML = "";

  const maleStudents = Object.values(students)
    .filter(student => student.sex === "male")
    .sort((a, b) => a.realName.localeCompare(b.realName));

  const femaleStudents = Object.values(students)
    .filter(student => student.sex === "female")
    .sort((a, b) => a.realName.localeCompare(b.realName));


  const maleHeading = document.createElement("h");
  maleHeading.innerHTML = `<div style="text-align: center";><i class="fa-solid fa-male">&nbsp;Male Students</i></div><br>`;
  studentListDiv.appendChild(maleHeading);

  maleStudents.forEach(student => {
    const studentDiv = document.createElement("div");
    studentDiv.classList.add("student-info");

    const studentName = document.createElement("h");
    studentName.innerHTML = `<i class="fa-solid fa-user"></i>&nbsp;${student.realName}`;
    const monthPresentDays = student[selectedMonth + "PresentDays"];
    const monthAbsentDays = student[selectedMonth + "AbsentDays"];

    const monthAttendance = document.createElement("li");
    monthAttendance.innerHTML = `<br><i class="fa-solid fa-check"></i>&nbsp;Days of Present: ${monthPresentDays}<br><i class="fa-solid fa-xmark"></i> &nbsp;Days of Absent: ${monthAbsentDays}`;
    
    monthAttendance.classList.add("Days");

    studentDiv.appendChild(studentName);
    studentDiv.appendChild(monthAttendance);
    studentListDiv.appendChild(studentDiv);
  });

  const femaleHeading = document.createElement("h");
  femaleHeading.innerHTML = `<hr><div style="text-align: center";><i class="fa-solid fa-female">&nbsp;Female Students</i></div><br>`;
  studentListDiv.appendChild(femaleHeading);

  femaleStudents.forEach(student => {
    const studentDiv = document.createElement("div");
    studentDiv.classList.add("student-info");

    const studentName = document.createElement("h");
    studentName.innerHTML = `<i class="fa-solid fa-user"></i>&nbsp;${student.realName}`;


    const monthPresentDays = student[selectedMonth + "PresentDays"];
    const monthAbsentDays = student[selectedMonth + "AbsentDays"];

    const monthAttendance = document.createElement("li");
    monthAttendance.innerHTML = `<br><i class="fa-solid fa-check"></i>&nbsp;Days of Present: ${monthPresentDays}<br><i class="fa-solid fa-xmark"></i> &nbsp;Days of Absent: ${monthAbsentDays}`;
    
    monthAttendance.classList.add("Days");

    studentDiv.appendChild(studentName);
    studentDiv.appendChild(monthAttendance);
    studentListDiv.appendChild(studentDiv);
  });
}



